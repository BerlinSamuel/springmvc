package exampleprograms;
public class ForLoopExample {
	public static void main(String[] args) {
		for(int i=1;i<=5;i++) {
			for(int j=5;j>=i;j--) {
				System.out.println("* ");
		}
			System.out.println();
		}
	for(int i=1;i<=5;i++){  
	for(int j=1;j<=i;j++){  
		        System.out.print("* ");  
		}  
		System.out.println();
		}
	String str[]= {"abc,bcd,cde,def,efg,fgh"};
	for(String string:str){
		System.out.println();
		System.out.println(string);
		}
		}  
		}